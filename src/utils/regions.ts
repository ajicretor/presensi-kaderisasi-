// Return flat list of all kabupaten/kota in Indonesia for backward compatibility or simple lists
export const ALL_KAB_KOTA = Object.values(INDONESIA_REGIONS).flat().sort();

// Helper to get province name by a kabupaten/kota
export function getProvinsiByKabKota(kabKotaName: string): string {
  const normalized = kabKotaName.trim().toUpperCase();
  for (const [prov, list] of Object.entries(INDONESIA_REGIONS)) {
    if (list.includes(normalized)) {
      return prov;
    }
  }
  return "JAWA BARAT"; // Default fallback
}
